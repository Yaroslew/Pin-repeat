int	ft_sqrt(int nb)
{
	int q;
	int b;

	q = 1;
	b = 0;
	if (nb <= 0)
		return (0);
	while (q != nb)
	{
		b = q * q;
		if (b == nb)
			return (q);
		else
			q++;
	}
	return (0);
}
