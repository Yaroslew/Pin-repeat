int	ft_iterative_factorial(int nb)
{
	long long int q;

	q = nb;
	if (nb == 0)
	{
		q = 1;
	}
	else if (nb < 0)
	{
		q = 0;
	}
	else
	{
		while (nb != 1)
		{
			q = q * (nb - 1);
			nb--;
		}
	}
	return (q);
}
